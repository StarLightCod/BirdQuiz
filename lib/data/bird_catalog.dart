  /// Получить отсортированный список русских названий
  static List<String> get russianNames {
    final names = russianToLatin.keys.toList();
    names.sort((a, b) => a.compareTo(b));
    return names;
  }

  /// Поиск по русскому или латинскому названию
  static List<MapEntry<String, String>> search(String query) {
    if (query.trim().isEmpty) {
      return russianToLatin.entries.toList()
        ..sort((a, b) => a.key.compareTo(b.key));
    }

    final q = query.toLowerCase().trim();
    final results = russianToLatin.entries.where((entry) {
      return entry.key.toLowerCase().contains(q) ||
             entry.value.toLowerCase().contains(q);
    }).toList();
    
    results.sort((a, b) => a.key.compareTo(b.key));
    return results;
  }

  /// Получить латинское название по русскому
  static String? getLatinName(String russian) {
    return russianToLatin[russian];
  }

